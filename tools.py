import os
import json
from datetime import datetime

# -----------------------
# 🌐 UPDATED SEARCH IMPORT
# -----------------------
try:
    from ddgs import DDGS
except ImportError:
    DDGS = None


# -----------------------
# 🛠 MAIN TOOL ROUTER
# -----------------------
def run_tool(action):
    act = action.get("action")
    inp = action.get("input")

    os.makedirs("workspace", exist_ok=True)

    # -----------------------
    # ✍️ WRITE
    # -----------------------
    if act == "write":
        return f"Generated content:\n{str(inp)}"

    # -----------------------
    # 🧠 ANALYZE
    # -----------------------
    elif act == "analyze":
        return f"Analysis:\n{str(inp)}"

    # -----------------------
    # 🌐 SEARCH (UPGRADED)
    # -----------------------
    elif act == "search":
        if DDGS is None:
            return "Search tool not installed. Run: pip install ddgs"

        try:
            query = str(inp).strip()

            # 🔥 AUTO-IMPROVE WEAK QUERIES
            if len(query.split()) < 4:
                query += " detailed explanation examples trends"

            results_text = []

            with DDGS() as ddgs:
                results = ddgs.text(query, max_results=5)

                for r in results:
                    results_text.append(
                        f"{r.get('title')}\n{r.get('href')}\n{r.get('body')}"
                    )

            # 🔥 FALLBACK IF EMPTY
            if not results_text:
                return f"No strong results found.\nRefined query used: {query}"

            return "Search results:\n\n" + "\n\n---\n\n".join(results_text)

        except Exception as e:
            return f"Search error: {str(e)}"

    # -----------------------
    # 📁 SIMPLE FILE SAVE
    # -----------------------
    elif act == "save_file":
        try:
            safe_name = str(inp)[:50].replace(" ", "_").replace("/", "_")
            filename = f"{safe_name}.txt"
            filepath = os.path.join("workspace", filename)

            content = f"""Generated by agent
Time: {datetime.now()}

Content:
{str(inp)}
"""

            with open(filepath, "w") as f:
                f.write(content)

            return f"File saved: {filepath}"

        except Exception as e:
            return f"File error: {str(e)}"

    # -----------------------
    # 📁 STRUCTURED SAVE
    # -----------------------
    elif act == "save_structured":
        try:
            filename = f"structured_{int(datetime.now().timestamp())}.txt"
            filepath = os.path.join("workspace", filename)

            # 🔥 FIX: ALWAYS STRING SAFE
            if isinstance(inp, (dict, list)):
                content = json.dumps(inp, indent=2)
            else:
                content = str(inp)

            with open(filepath, "w") as f:
                f.write(content)

            return f"Structured file saved: {filepath}"

        except Exception as e:
            return f"File error: {str(e)}"

    # -----------------------
    # ❓ UNKNOWN
    # -----------------------
    return f"Unknown action: {act}"
