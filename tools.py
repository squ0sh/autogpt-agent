import os
import json
from datetime import datetime

# -----------------------
# 🌐 SAFE IMPORT
# -----------------------
try:
    from duckduckgo_search import DDGS
except ImportError:
    DDGS = None


# -----------------------
# 🛠 MAIN TOOL ROUTER
# -----------------------
def run_tool(action):
    act = action.get("action")
    inp = action.get("input")

    # Ensure workspace exists
    os.makedirs("workspace", exist_ok=True)

    # -----------------------
    # ✍️ WRITE
    # -----------------------
    if act == "write":
        return f"Generated content: {str(inp)}"

    # -----------------------
    # 🧠 ANALYZE
    # -----------------------
    elif act == "analyze":
        return f"Analysis: {str(inp)}"

    # -----------------------
    # 🌐 REAL SEARCH 🔥
    # -----------------------
    elif act == "search":
        if DDGS is None:
            return "Search tool not installed. Install duckduckgo-search."

        try:
            results_text = []

            with DDGS() as ddgs:
                results = ddgs.text(str(inp), max_results=5)

                for r in results:
                    results_text.append(
                        f"{r.get('title')}\n{r.get('href')}\n{r.get('body')}"
                    )

            if not results_text:
                return "No search results found."

            return "Search results:\n\n" + "\n\n---\n\n".join(results_text)

        except Exception as e:
            return f"Search error: {str(e)}"

    # -----------------------
    # 📁 SIMPLE FILE SAVE
    # -----------------------
    elif act == "save_file":
        try:
            safe_name = str(inp)[:50].replace(" ", "_").replace("/", "_")
            filename = f"{safe_name}.txt"
            filepath = os.path.join("workspace", filename)

            content = f"""Generated by agent
Time: {datetime.now()}

Content:
{str(inp)}
"""

            with open(filepath, "w") as f:
                f.write(content)

            return f"File saved: {filepath}"

        except Exception as e:
            return f"File error: {str(e)}"

    # -----------------------
    # 📁 STRUCTURED SAVE 🔥🔥
    # -----------------------
    elif act == "save_structured":
        try:
            filename = f"structured_{int(datetime.now().timestamp())}.txt"
            filepath = os.path.join("workspace", filename)

            # 🔥 CRITICAL FIX (handles dict/list/string)
            if isinstance(inp, (dict, list)):
                content = json.dumps(inp, indent=2)
            else:
                content = str(inp)

            with open(filepath, "w") as f:
                f.write(content)

            return f"Structured file saved: {filepath}"

        except Exception as e:
            return f"File error: {str(e)}"

    # -----------------------
    # ❓ UNKNOWN ACTION
    # -----------------------
    return f"Unknown action: {act}"
